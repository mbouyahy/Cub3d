#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		j;
	char	*s;
	char	*d;

	i = 0;
	j = 0;
	s = (char *)s1;
	while (s[j])
		j++;
	d = (char *)malloc(j + 1);
	if (!d)
		return (NULL);
	while (s[i])
	{
		d[i] = s[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
